    private void InfoTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_InfoTableMouseClicked
if(state.equals("Playing")){
        try {
            audioPlayer.stop();
        } catch (Exception ex) {
            Logger.getLogger(MusicPlayerScreen.class.getName()).log(Level.SEVERE, null, ex);
        }
        int row = InfoTable.getSelectedRow();
        int songid = (int) InfoTable.getValueAt(row, 0);
        String songName = (String) InfoTable.getValueAt(row, 1);
        String artistName = (String) InfoTable.getValueAt(row, 2);
        String genreName = (String) InfoTable.getValueAt(row, 3);
        String songPath = (String) InfoTable.getValueAt(row, 4);
       
        audioPlayer.setFilePath(db.getPath(songid));
        try {
            audioPlayer.setStruct();
        } catch (Exception ex) {
            Logger.getLogger(MusicPlayerScreen.class.getName()).log(Level.SEVERE, null, ex);
        }
        state = "Playing";
        PlayButton.setEnabled(false);
        PauseButton.setEnabled(true);
        CurrentSong.setText(songName + " - " + artistName);
    
        curSongName = songName;
        curArtistName = artistName;
        curGenreName = genreName;
        curSongPath = songPath;
        curSong = songid;
        
     }
    }//GEN-LAST:event_InfoTableMouseClicked

