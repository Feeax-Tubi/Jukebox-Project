    private void DeleteSongButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteSongButtonActionPerformed
         if(state.equals("Playing")){
            try {
                audioPlayer.stop();
            } catch (Exception ex) {
                Logger.getLogger(MusicPlayerScreen.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        db.deleteSongFromUser(curSong,username);
        MusicPlayerScreen musicplayerscreen;
        try 
        {
            musicplayerscreen = new MusicPlayerScreen(username,userState);
            musicplayerscreen.setVisible(true);
            this.dispose();
        } catch (Exception ex) {
            Logger.getLogger(UserPlayScreen.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_DeleteSongButtonActionPerformed

